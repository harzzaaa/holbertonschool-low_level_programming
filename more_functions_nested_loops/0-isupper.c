/**
 * _isupper - Check for uppercase character
 * @c : Entry check
 * Return: returns 1 if uppercase otherwise 0
*/

int _isupper(int c)
{
	char ch = 'c';

	if (_isupper(ch))
	{
		printf("%c Is Uppercase \n", ch);
	}
	else
	{
		printf("%c Is Not Uppercase \n", ch);
	}
	return ("0 \n");
}
